class AudioLDM2Dataset(Dataset):
    def __init__(
        self,
        metadata_dir,
        tokenizer,
        tokenizer_2,
        size=600,
    ):
        self.metadata_dir = metadata_dir
        self.tokenizer = tokenizer
        self.tokenizer_2 = tokenizer_2
        self.size = size

        self.emotions, self.phonemes, self.mel_dirs = self.load_data()

    def __len__(self):
        return len(self.mel_dirs)

    def __getitem__(self, i):
        example = {}
        mel_dir = self.mel_dirs[i]
        phoneme = self.phonemes[i] + " ."
        #emotion = self.emotions[i]

        prompt_text = random.choice(prompt_test)

        prompt_inputs = self.tokenizer(
            prompt_text,
            padding="max_length",
            max_length=self.tokenizer.model_max_length,
            truncation=True,
            return_tensors="pt",
        )
        example["prompt_input_ids"] = prompt_inputs.input_ids
        example["prompt_attention_mask"] = prompt_inputs.attention_mask

        transcription_inputs = self.tokenizer_2(phoneme, return_tensors="pt", padding="max_length", truncation=True)
        example["transcript_input_ids"] = transcription_inputs.input_ids
        example["transcript_attention_mask"] = transcription_inputs.attention_mask

        mel = np.load(mel_dir)
        mel = torch.from_numpy(mel)[:,:1000]

        example["mel"] = mel.transpose(0, 1).unsqueeze(0)

        return example

    def load_data(self):
        emotions, phonemes, mel_dirs = [], [], []
        with open(self.metadata_dir, "r") as f:
            for line in f.readlines():
                line = line.strip().split("|")
                mel_dirs.append(line[0])
                phonemes.append(line[1])
                #emotions.append(line[2])
        
        return emotions, phonemes, mel_dirs


